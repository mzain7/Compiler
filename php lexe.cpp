enum State
{
    KEYWORD,
    IDENTIFIER,
    OPERATOR,
    COMMENT,
    LITERAL,
    PUNCTUATION
};
const char *getStateName(State state)
{
    switch (state)
    {
    case KEYWORD:
        return "KEYWORD";
    case IDENTIFIER:
        return "IDENTIFIER";
    case OPERATOR:
        return "OPERATOR";
    case COMMENT:
        return "COMMENT";
    case LITERAL:
        return "LITERAL";
    case PUNCTUATION:
        return "PUNCTUATION";
    default:
        return "UNKNOWN";
    }
}

struct Token
{
    string lexeme;
    State type;
    Token *next;
    Token(string lexeme, State type)
    {
        this->lexeme = lexeme;
        this->type = type;
        this->next = NULL;
    }
};

class LinkedList
{
private:
    Token *head = nullptr;
    Token *tail = nullptr;

public:
    LinkedList()
    {
        head = nullptr;
        tail = nullptr;
    }

    void insertToken(string lexeme, State type)
    {
        Token *newToken = new Token(lexeme, type);
        if (head == nullptr)
        {
            head = newToken;
            tail = newToken;
        }
        else
        {
            tail->next = newToken;
            tail = newToken;
        }
    }

    Token *removeToken()
    {
        Token *ret = head;
        if (head->next != NULL)
        {
            head = head->next;
        }
        else
        {
            head = nullptr;
            tail = nullptr;
        }

        return ret;
    }

    void print(ofstream &file)
    {
        Token *tmp = head;

        while (tmp != nullptr)
        {
            file << "Token: " << tmp->lexeme << ", Token Type: " << getStateName(tmp->type) << endl;
            tmp = tmp->next;
        }
    }
};



